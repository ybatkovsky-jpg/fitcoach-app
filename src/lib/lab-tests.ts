export function getLifestyleRecommendations(results: Record<string, number>): LifestyleAdvice[] {
  // Return advice relevant to biomarkers that are out of range
  const triggered = new Set<string>();
  const advice: LifestyleAdvice[] = [];

  for (const b of BIOMARKERS) {
    const value = results[b.id];
    if (value === undefined) continue;
    const status = getBiomarkerStatus(value, b);
    if (status !== 'normal' && status !== 'ideal') {
      for (const a of LIFESTYLE_ADVICE) {
        if (a.affectedBiomarkerIds.includes(b.id) && !triggered.has(a.title)) {
          triggered.add(a.title);
          advice.push(a);
        }
      }
    }
  }

  return advice;
}