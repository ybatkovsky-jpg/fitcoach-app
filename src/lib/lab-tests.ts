export type BiomarkerStatus = 'low' | 'normal' | 'high' | 'ideal';

export function getBiomarkerStatus(value: number, b: BiomarkerDef): BiomarkerStatus {
  if (value < b.lowThreshold) return 'low';
  if (value > b.highThreshold) return 'high';
  if (b.idealMin !== undefined && b.idealMax !== undefined && value >= b.idealMin && value <= b.idealMax) {
    return 'ideal';
  }
  return 'normal';
}

